.nolist
.include "m328Pdef.inc"
.list

INICIO:
	LDI R16, 0b11111111
	OUT DDRD, R16
	LDI R16, 0b00000000 

PRINCIPAL:
	OUT PORTD, R16
	CALL ATRASO
	CALL INCREMENTAR
	RJMP PRINCIPAL

INCREMENTAR:
	CPI R16, 15
	BREQ ZERAR
		INC R16
		RET
	ZERAR:
		LDI R16, 0
		RET

ATRASO:
    LDI R17, 82 
    L17:
        LDI R18, 255
    L18:
        LDI R19, 255
    L19:
        DEC R19
        BRNE L19
        DEC R18
        BRNE L18
        DEC R17
        BRNE L17
    RET 