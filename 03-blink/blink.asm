.nolist
.include "m328Pdef.inc"
.list

INICIO:
	LDI R16, 0b11111111
	OUT DDRD, R16
	LDI R16, 0b00000000
	OUT PORTD, R16

PRINCIPAL:
	SBI PORTD, PD0
	CALL ATRASO	
	CBI PORTD, PD0
	CALL ATRASO
	RJMP PRINCIPAL


ATRASO:
    LDI R17, 82 
	L1:
		LDI R18, 255
	L2:
		LDI R19, 255
	L3:
		DEC R19
		BRNE L3
		DEC R18
		BRNE L2
		DEC R17
		BRNE L1

		RET
